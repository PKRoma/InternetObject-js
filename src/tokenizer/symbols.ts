
/**
 * Enumeration representing special symbols in IO.
 */
enum Symbols {
  CURLY_OPEN      = '{',
  CURLY_CLOSE     = '}',
  BRACKET_OPEN    = '[',
  BRACKET_CLOSE   = ']',
  COLON           = ':',
  COMMA           = ',',
  R               = 'r', // raw string
  B               = 'b', // binary
  DT              = 'dt', // datetime
  D               = 'd', // date
  T               = 't', // time
  HASH            = '#',
  DOUBLE_QUOTE    = '"',
  SINGLE_QUOTE    = "'",
  BACKSLASH       = "\\",
  TILDE           = '~',
  PLUS            = '+',
  MINUS           = '-',
}

export default Symbols;